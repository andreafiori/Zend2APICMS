<?php

namespace ModelModule\Model;

class NullException extends \Exception
{
    private $params;

    /**
     * @param array $params
     */
    public function setParams(array $params)
    {
        $this->params = $params;
    }

    /**
     * @return array
     */
    public function getParams()
    {
        return $this->params;
    }
}
